package cn.segema.learn.util;

public class PersonDomain {
	private int age;
	private String name;

	public PersonDomain(int age, String name) {
		super();
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
